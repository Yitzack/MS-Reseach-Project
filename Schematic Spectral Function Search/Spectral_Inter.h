void Spectral_Inter::Characterize(long double P, pair<long double, long double>& zero)
{
	long double M_old = Q(P,Parameters[1][0],Parameters[1][1],Parameters[1][2]);
	long double M_new;
	long double h = .00001;
	int i = 0;

	M_new = M_old - h*(1./Spectral(pow(M_old+h,2), P)-1./Spectral(pow(M_old,2), P))/(1./Spectral(pow(M_old-h,2), P)-2.*1./Spectral(pow(M_old,2), P)+1./Spectral(pow(M_old+h,2), P));

	while(abs(M_new/M_old-1.) > 1e-5 && i < 20)
	{
		M_old = M_new;
		M_new = M_old - h*(1./Spectral(pow(M_old+h,2), P)-1./Spectral(pow(M_old,2), P))/(1./Spectral(pow(M_old-h,2), P)-2./Spectral(pow(M_old,2), P)+1./Spectral(pow(M_old+h,2), P));
		i++;
	}

	zero.first = M_new;
	zero.second = 2.*sqrt(-pow(h,2)*Spectral(pow(M_new,2), P)/(2.*(Spectral(pow(M_new-h,2), P)-2.*Spectral(pow(M_new,2), P)+Spectral(pow(M_new+h,2), P))));

	if(isnan(zero.second) || M_new < 2.5 || M_new > 4.5)
	{
		zero.first = Q(P,Parameters[1][0],Parameters[1][1],Parameters[1][2]);
		zero.second = Q(P,Parameters[2][0],Parameters[2][1],Parameters[2][2])*Width(zero.first,P);
	}
}

